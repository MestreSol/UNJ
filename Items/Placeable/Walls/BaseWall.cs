using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Terraria;
using Terraria.ID;
using Terraria.ModLoader;
using Terraria.Localization;
using UNJ.Walls;
namespace UNJ.Items.Placeable.Walls
{
    class BaseWall : ModItem
    {
        public override void SetStaticDefaults()
        {
            Tooltip.SetDefault("Teste de parede");
        }
        public override void SetDefaults()
        {
            item.width = 12;
            item.height = 12;
            item.maxStack = 999;
            item.useTurn = true;
            item.autoReuse = true;
            item.useAnimation = 15;
            item.useTime = 7;
            item.useStyle = ItemUseStyleID.SwingThrow;
            item.consumable = true;
            item.createWall = ModContent.WallType<BaseWalls>();
        }
    }
}
